
package com.emergentes.modelo;
public class Categoria {
    private int id;
    private String Categoria;

    public Categoria() {
        id=0;
        Categoria="";
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCategoria() {
        return Categoria;
    }

    public void setCategoria(String Categoria) {
        this.Categoria = Categoria;
    }
    
    
}
